package org.example;

public class Main {
    public static void main(String[] args) {

    //    System.out.println("Hello world!");
        for (int x=1;x<=12;x++) {
            System.out.println(x);
        }
        for (int y=1;y<=6;y++) {
            System.out.println(y*2);
        }

        //Uso de While
        int z=1;
        while (z<=12) {
            System.out.println(z);
            z++;
        }
    }
}